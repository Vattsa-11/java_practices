public class Q5 {
    public static void main(String[] arg){
        int a = 10;
        System.out.print("Before pre increment: "+ a);
        System.out.print("\nAt pre increment: "+ ++a);
        System.out.print("\nAfter pre increment: "+ a);

        System.out.print("\nBefore post increment: "+ a);
        System.out.print("\nAt post increment: "+ a++);
        System.out.print("\nAfter post increment: "+ a);
    }
} 
